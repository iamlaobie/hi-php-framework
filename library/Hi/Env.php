<?php
class Hi_Env
{
    /**
     * 应用的标识
     * @var unknown_type
     */
    public static $APPID;
    /**
     * 是否开启缓存
     * @var unknown_type
     */
    public static $CACHEABLE;
    /**
     * http服务器的document root
     * @var string
     */
    public static $DOC_ROOT;
    /**
     * 框架入口文件的绝对路径
     * @var string
     */
    public static $ENTRY_FILE;
    /**
     * app的路径
     * @var string
     */
    public static $PATH_APP;
    /**
     * class的路径
     * @var string
     */
    public static $PATH_CLASS;
    /**
     * 临时文件路径
     * @var unknown_type
     */
    public static $PATH_TMP;
    /**
     * 文件缓存的路径
     * @var unknown_type
     */
    public static $PATH_CACHE;
    /**
     * 日志路径
     * @var unknown_type
     */
    public static $PATH_LOG;
    /**
     * 是否debug
     * @var unknown_type
     */
    public static $DEBUG;
}